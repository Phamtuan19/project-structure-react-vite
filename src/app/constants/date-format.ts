/**
 * The default date format used for displaying dates in the UI.
 * Example: 25/04/2025
 */
export const DATE_FORMAT = 'DD/MM/YYYY';

/**
 * The date format used for displaying both date and time in the UI.
 * Example: 25/04/2025 10:30
 */
export const DATE_TIME_FORMAT = 'DD/MM/YYYY HH:mm';

/**
 * The standard date format used for API requests and responses.
 * Example: 2025-04-25
 */
export const API_DATE_FORMAT = 'YYYY-MM-DD';

/**
 * The date and time format with '-' separating date and time.
 * Example: 25-04-2025-10-30
 */
export const DATE_TIME_DASH_FORMAT = 'DD-MM-YYYY-HH-mm';
